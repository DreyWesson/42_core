#!/bin/bash

tens=(
    # "15 30 13 29 27 7 18 26 21 12"
    # "8 22 11 25 27 23 12 2 21 14"
    # "20 25 29 7 10 23 11 12 26 27"
    # "20 15 14 26 19 2 21 23 24 4"
    # "18 15 4 1 19 8 27 24 9 13"
    # "8 11 23 7 17 9 12 22 13 24"
    # "29 9 13 15 0 6 12 24 8 27"
    # "4 10 5 2 11 20 13 21 0 14"
    # "10 9 27 20 24 19 8 11 30 13"
    # "3 30 5 16 13 28 15 18 9 29"
)
n=$4
if [[ $1 == '' ]]; then
    for i in ${!tens[@]}; do
        echo "${tens[$i]}"
        ./tester.sh .. -co ${tens[$i]}
    done
elif [[ $1 != '' && $2 != '' && $3 != '' ]]; then
    for ((i=0;i<n;i++))
    do
        ./tester.sh .. -r $1 $2 $3
    done
fi